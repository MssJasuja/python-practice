#Implicit function
a = 10                         #integer
b = 4.7                        #float
result = a + b
print("Result of a + b =", result)
print(type(result))

#Explicit function
x = "3"                         #its string function
y = int(x)                      #convert string to integer
print(y)
print(type(y))

#Conversion data types
num = int(9.8)                   #float to integer
print("Integer value of 9.8 =" , num)
print(type(num))
